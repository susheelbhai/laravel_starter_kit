export default function ContinueWithSocial({ 
    platform, 
    href 
}: { 
    platform: SocialPlatform; 
    href?: string;
}) {
    const config = socialConfigs[platform];
    
    if (!config) {
        return null;
    }

    const handleClick = () => {
        window.location.href = href ?? route('social.login', platform);
    };

    return (
        <div className="mb-1">
            <Button
                type="button"
                variant="outline"
                className="w-full"
                onClick={handleClick}
            >
                {config.icon}
                {config.label}
            </Button>
        </div>
    );
}
